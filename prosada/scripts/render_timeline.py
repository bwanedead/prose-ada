def _collect_subtree_ids(
    scope_id: str,
    units: Dict[str, Any],
) -> Set[str]:
    """Return the set of unitIds reachable from scope_id (inclusive)."""
    result: Set[str] = set()

    def _walk(uid: str) -> None:
        if uid in result:
            return
        result.add(uid)
        unit = units.get(uid)
        if unit:
            for child_id in (unit.children or []):
                _walk(child_id)

    _walk(scope_id)
    return result


def _filter_canvas_by_scope(
    canvas: Dict[str, Any],
    scope_ids: Set[str],
) -> Dict[str, Any]:
    """
    Filter events, checkpoints, and optionally timelines/threads to only include
    units within the scope set.

    Event IDs are `ev-{unitId}` or `ev-{unitId}--{streamId}`.
    """
    def _unit_id_from_event_id(ev_id: str) -> Optional[str]:
        if not ev_id.startswith("ev-"):
            return None
        return ev_id[3:].split("--")[0]

    filtered_events = {
        eid: e for eid, e in canvas["events"].items()
        if _unit_id_from_event_id(eid) in scope_ids
    }
    filtered_checkpoints = {
        cid: c for cid, c in canvas["checkpoints"].items()
        if cid.lstrip("cp-") in scope_ids or
           cid[3:] in scope_ids  # cp-{unitId}
    }
    return {
        "timelines": canvas["timelines"],
        "threads": canvas["threads"],
        "events": filtered_events,
        "checkpoints": filtered_checkpoints,
    }


def _filter_canvas_by_streams(
    canvas: Dict[str, Any],
    stream_ids: Set[str],
) -> Dict[str, Any]:
    """Keep only threads whose ID is in stream_ids, and drop their event copies."""
    filtered_threads = {
        tid: t for tid, t in canvas["threads"].items()
        if tid in stream_ids
    }
    # Keep spine events (laneId=0) and stream copies only for included streams
    filtered_events = {
        eid: e for eid, e in canvas["events"].items()
        if e.get("laneId", 0) == 0 or e.get("attachedToId") in stream_ids
    }
    return {
        "timelines": canvas["timelines"],
        "threads": filtered_threads,
        "events": filtered_events,
        "checkpoints": canvas["checkpoints"],
    }


def _filter_canvas_by_status(
    canvas: Dict[str, Any],
    status: str,
) -> Dict[str, Any]:
    """
    Filter events by their isResolved flag.
    status="locked" → only locked (isResolved=True) events.
    status="draft" or "review" → only non-locked events.
    """
    if status == "locked":
        filtered = {eid: e for eid, e in canvas["events"].items() if e.get("isResolved")}
    else:
        filtered = {eid: e for eid, e in canvas["events"].items() if not e.get("isResolved")}
    return {
        "timelines": canvas["timelines"],
        "threads": canvas["threads"],
        "events": filtered,
        "checkpoints": canvas["checkpoints"],
    }


def _apply_canvas_filters(
    canvas: Dict[str, Any],
    units: Dict,
    scope: Optional[str],
    streams: Optional[List[str]],
    status: Optional[str],
) -> Dict[str, Any]:
    """Apply scope/stream/status filters to a composed canvas dict."""
    if scope:
        scope_ids = _collect_subtree_ids(scope, units)
        canvas = _filter_canvas_by_scope(canvas, scope_ids)
    if streams is not None:
        canvas = _filter_canvas_by_streams(canvas, set(streams))
    if status:
        canvas = _filter_canvas_by_status(canvas, status)
    return canvas



def _esc(s: str) -> str:
    """XML-escape a string for safe SVG embedding."""
    return (s.replace("&", "&amp;")
             .replace("<", "&lt;")
             .replace(">", "&gt;")
             .replace('"', "&quot;")
             .replace("'", "&#x27;"))


class SVGTimelineRenderer:
    """
    SVG output variant of the timeline renderer.

    Geometry is identical to TimelineRenderer (same constants, same lane model).
    Reuses the same canvas dict from CanvasLayoutComposer — no layout duplication.
    """

    def __init__(
        self,
        canvas: Dict[str, Any],
        scale: int = 80,
        img_width: Optional[int] = None,
        title: Optional[str] = None,
    ):
        self.canvas = canvas
        self.scale = scale
        self.title = title

        max_end = self._compute_max_end()
        content_width = max(int(max_end * scale) + 1, 10)
        self.img_width = img_width if img_width else content_width + MARGIN_LEFT + MARGIN_RIGHT

        timelines = canvas.get("timelines", {})
        threads = canvas.get("threads", {})
        self.lane_objects: Dict[int, dict] = {}

        for tl in timelines.values():
            lid = tl.get("laneId", 0)
            self.lane_objects[lid] = {"color": "#4a4a6a", **tl}
        for th in threads.values():
            lid = th.get("laneId", 1)
            self.lane_objects[lid] = th

        max_lane = max(self.lane_objects.keys(), default=0)
        self.num_lanes = max_lane + 1
        lane_area = self.num_lanes * (LANE_HEIGHT + LANE_GAP)
        self.img_height = MARGIN_TOP + lane_area + MARGIN_BOTTOM

    def _compute_max_end(self) -> float:
        ends: List[float] = [1.0]
        for obj in self.canvas.get("timelines", {}).values():
            ends.append(obj.get("position", 0) + obj.get("duration", 1))
        for obj in self.canvas.get("threads", {}).values():
            ends.append(obj.get("position", 0) + obj.get("duration", 1))
        for obj in self.canvas.get("events", {}).values():
            ends.append(obj.get("position", 0) + obj.get("duration", 1))
        return max(ends)

    def _lane_y(self, lane_id: int) -> int:
        return MARGIN_TOP + lane_id * (LANE_HEIGHT + LANE_GAP)

    def _spine_x(self, position: float) -> int:
        return MARGIN_LEFT + int(position * self.scale)

    def _spine_w(self, duration: float) -> int:
        return max(2, int(duration * self.scale))

    def render_svg(self, overlay_data: Optional[Dict[str, Dict]] = None) -> str:
        """Produce and return a complete SVG document string.

        Parameters
        ----------
        overlay_data : Optional per-event overlay props (from compute_overlay).
        """
        od_map = overlay_data or {}
        W, H = self.img_width, self.img_height
        lines: List[str] = []

        lines.append(
            f'<svg xmlns="http://www.w3.org/2000/svg" '
            f'width="{W}" height="{H}" '
            f'viewBox="0 0 {W} {H}" '
            f'style="background:{_rgb_css(BG_COLOR)}">'
        )
        lines.append(f'<rect width="{W}" height="{H}" fill="{_rgb_css(BG_COLOR)}"/>')

        # Grid lines
        max_end = self._compute_max_end()
        for i in range(0, int(max_end) + 2):
            x = self._spine_x(i)
            if x > W - MARGIN_RIGHT:
                break
            r, g, b = GRID_COLOR
            lines.append(
                f'<line x1="{x}" y1="{MARGIN_TOP}" x2="{x}" y2="{H - MARGIN_BOTTOM}" '
                f'stroke="rgb({r},{g},{b})" stroke-width="1"/>'
            )
            tr, tg, tb = TICK_COLOR
            lines.append(
                f'<text x="{x + 2}" y="{MARGIN_TOP - 4}" '
                f'font-size="10" fill="rgb({tr},{tg},{tb})">{i}</text>'
            )

        # Title
        if self.title:
            lr, lg, lb = LABEL_FG
            lines.append(
                f'<text x="{MARGIN_LEFT}" y="20" font-size="13" '
                f'font-weight="bold" fill="rgb({lr},{lg},{lb})">'
                f'{_esc(self.title)}</text>'
            )

        # Lane tracks
        for lane_id, obj in self.lane_objects.items():
            y = self._lane_y(lane_id)
            pos = obj.get("position", 0)
            dur = obj.get("duration", 1)
            raw_color = obj.get("color", "#888888")
            try:
                r, g, b = _hex_to_rgb(raw_color)
            except Exception:
                r, g, b = 128, 128, 128
            x = self._spine_x(pos)
            w = self._spine_w(dur)
            alpha = round(THREAD_TRACK_ALPHA / 255, 2)
            lines.append(
                f'<rect x="{x}" y="{y}" width="{w}" height="{LANE_HEIGHT}" '
                f'fill="rgb({r},{g},{b})" fill-opacity="{alpha}"/>'
            )
            lines.append(
                f'<line x1="{x}" y1="{y}" x2="{x + w}" y2="{y}" '
                f'stroke="rgb({r},{g},{b})" stroke-opacity="0.7" stroke-width="1"/>'
            )

        # Build lane color map
        lane_colors: Dict[int, Tuple[int, int, int]] = {}
        for lane_id, obj in self.lane_objects.items():
            try:
                lane_colors[lane_id] = _hex_to_rgb(obj.get("color", "#888888"))
            except Exception:
                lane_colors[lane_id] = (128, 128, 128)

        # Events
        events_sorted = sorted(
            self.canvas.get("events", {}).values(),
            key=lambda e: (e.get("laneId", 0) != 0),
        )
        for event in events_sorted:
            ev_id = event.get("id", "")
            od = od_map.get(ev_id, {})

            lane_id = event.get("laneId", 0)
            y_lane = self._lane_y(lane_id)
            pos = event.get("position", 0)
            dur = event.get("duration", 1)
            is_locked = event.get("isResolved", False)

            # Base color
            base_rgb = lane_colors.get(lane_id, (100, 120, 180))
            r, g, b = _lighten(base_rgb, 1.8)

            # Overlay overrides
            if "fill_rgb" in od:
                r, g, b = od["fill_rgb"]
            opacity: float = od.get("opacity", 1.0)
            ev_alpha = round((EVENT_ALPHA / 255) * opacity, 2)

            x = self._spine_x(pos)
            w = self._spine_w(dur)
            y_ev = y_lane + EVENT_Y_OFFSET
            h_ev = EVENT_HEIGHT

            if "outline_rgb" in od:
                or_, og_, ob_ = od["outline_rgb"]
                stroke = f"rgb({or_},{og_},{ob_})"
            elif is_locked:
                stroke = "#FFD700"
            else:
                stroke = f"rgb({r},{g},{b})"
            stroke_w = od.get("outline_width", 2 if is_locked else 1)

            lines.append(
                f'<rect x="{x}" y="{y_ev}" width="{w}" height="{h_ev}" '
                f'fill="rgb({r},{g},{b})" fill-opacity="{ev_alpha}" '
                f'stroke="{stroke}" stroke-width="{stroke_w}"/>'
            )
            title_text = event.get("title", "")
            if title_text and w > 12:
                max_chars = max(1, (w - 4) // 6)
                label = title_text[:max_chars] + ("…" if len(title_text) > max_chars else "")
                lr, lg, lb = LABEL_FG
                lines.append(
                    f'<text x="{x + 3}" y="{y_ev + 12}" font-size="9" '
                    f'fill="rgb({lr},{lg},{lb})">{_esc(label)}</text>'
                )

            # Badge text (e.g. shot type abbreviation from overlay)
            badge = od.get("badge_text")
            if badge and w > 8:
                bw = len(badge) * 6 + 4
                bh = 9
                bx = x + w - bw - 2
                by = y_ev + h_ev - bh - 1
                if bx >= x:
                    lines.append(
                        f'<rect x="{bx}" y="{by}" width="{bw}" height="{bh}" '
                        f'fill="rgba(0,0,0,0.78)" rx="1"/>'
                    )
                    lines.append(
                        f'<text x="{bx + 2}" y="{by + 8}" font-size="7" '
                        f'fill="rgb(255,255,200)">{_esc(badge)}</text>'
                    )

        # Checkpoints (diamonds)
        for cp in self.canvas.get("checkpoints", {}).values():
            pos = cp.get("position", 0)
            cx = self._spine_x(pos)
            cy = self._lane_y(0) + LANE_HEIGHT // 2
            s = CHECKPOINT_SIZE
            pts = f"{cx},{cy - s} {cx + s},{cy} {cx},{cy + s} {cx - s},{cy}"
            r, g, b = CHECKPOINT_COLOR
            lines.append(
                f'<polygon points="{pts}" fill="rgb({r},{g},{b})" '
                f'stroke="white" stroke-width="1"/>'
            )

        # Lane labels
        for lane_id, obj in sorted(self.lane_objects.items()):
            y = self._lane_y(lane_id) + LANE_HEIGHT // 2 + 4
            name = obj.get("name", obj.get("id", f"Lane {lane_id}"))
            max_chars = (MARGIN_LEFT - 8) // 6
            label = name[:max_chars] if len(name) > max_chars else name
            try:
                r, g, b = _hex_to_rgb(obj.get("color", "#888888"))
            except Exception:
                r, g, b = 160, 160, 180
            lines.append(
                f'<text x="4" y="{y}" font-size="10" fill="rgb({r},{g},{b})">'
                f'{_esc(label)}</text>'
            )

        lines.append("</svg>")
        return "\n".join(lines)


def _rgb_css(rgb: Tuple[int, int, int]) -> str:
    return f"rgb({rgb[0]},{rgb[1]},{rgb[2]})"



def main(argv: Optional[List[str]] = None) -> int:
    import json as _json

    parser = _build_parser()
    args = parser.parse_args(argv)

    project_path = Path(args.project).expanduser().resolve()
    out_path = Path(args.out).expanduser().resolve()
    fmt = args.format.lower()

    streams: Optional[List[str]] = None
    if args.streams:
        streams = [s.strip() for s in args.streams.split(",") if s.strip()]

    needs_projection = bool(args.projection_json or args.overlay or args.prompt_export)

    # Always load project explicitly so overlay code can access canvas
    try:
        units, manifest, registries_raw, canvas_raw = _load_and_compose(project_path)
    except FileNotFoundError as exc:
        print(f"Error: {exc}", file=sys.stderr)
        return 1
    except ValueError as exc:
        print(f"Error: {exc}", file=sys.stderr)
        return 2

    canvas = _apply_canvas_filters(canvas_raw, units, args.scope, streams, args.status)

    # Build projection payload if needed
    projection: Optional[Dict] = None
    if needs_projection:
        try:
            from services.projection_builder import build_projection
            projection = build_projection(
                project_path,
                scope=args.scope,
                streams=streams,
                status=args.status,
                semantic_kind=args.semantic_kind,
                semantic_entity_id=args.semantic_id,
            )
        except Exception as exc:
            print(f"Warning: projection build failed: {exc}", file=sys.stderr)

    # Write prompt packet export if requested
    if args.prompt_export and projection is not None:
        try:
            from services.prompt_exporter import export_prompt_packets
            packets = export_prompt_packets(
                project_path,
                profile=args.prompt_profile,
                scope=args.scope,
                streams=streams,
                status=args.status,
            )
            pe_out = Path(args.prompt_export).expanduser().resolve()
            pe_out.parent.mkdir(parents=True, exist_ok=True)
            pe_out.write_text(_json.dumps(packets, indent=2), encoding="utf-8")
            print(f"Saved prompt export: {pe_out}  ({packets['summary']['packetCount']} packets)")
        except Exception as exc:
            print(f"Warning: prompt export failed: {exc}", file=sys.stderr)

    # Write projection JSON if requested
    if args.projection_json and projection is not None:
        proj_out = Path(args.projection_json).expanduser().resolve()
        proj_out.parent.mkdir(parents=True, exist_ok=True)
        proj_out.write_text(_json.dumps(projection, indent=2), encoding="utf-8")
        print(f"Saved projection JSON: {proj_out}")

    # Compute overlay data
    overlay_data: Optional[Dict[str, Dict]] = None
    if args.overlay:
        if projection is not None:
            overlay_data = compute_overlay(
                args.overlay,
                canvas,
                projection,
                semantic_kind=args.semantic_kind,
                semantic_entity_id=args.semantic_id,
            )
        else:
            print(
                f"Warning: --overlay {args.overlay} requested but projection build failed; "
                "rendering without overlay.",
                file=sys.stderr,
            )

    render_kwargs: Dict[str, Any] = dict(
        scale=args.scale,
        img_width=args.img_width,
        title=args.title or str(project_path.name),
    )

    # Render
    try:
        if fmt == "svg":
            r_svg = SVGTimelineRenderer(canvas, **render_kwargs)
            output_svg = r_svg.render_svg(overlay_data=overlay_data)
            r_width, r_height, r_lanes = r_svg.img_width, r_svg.img_height, r_svg.num_lanes
            r_max_end = r_svg._compute_max_end()
        else:
            r_png = TimelineRenderer(canvas, **render_kwargs)
            output_png = r_png.render(overlay_data=overlay_data)
            r_width, r_height, r_lanes = r_png.img_width, r_png.img_height, r_png.num_lanes
            r_max_end = r_png._compute_max_end()
    except Exception as exc:
        print(f"Error: rendering failed: {exc}", file=sys.stderr)
        return 2

    # Sidecar
    sidecar: Optional[dict] = None
    if args.sidecar:
        overlay_matched = len([k for k, v in (overlay_data or {}).items() if v.get("opacity", 1.0) >= 1.0 and "fill_rgb" not in v]) if overlay_data else None
        sidecar = build_render_sidecar(
            project_path, canvas, r_width, r_height, r_lanes, r_max_end,
            scope=args.scope, streams=streams, status=args.status,
            scale=args.scale, format=fmt,
            overlay_mode=args.overlay,
            overlay_matched_count=len(overlay_data) if overlay_data else None,
        )

    # Write output
    out_path.parent.mkdir(parents=True, exist_ok=True)

    if fmt == "svg":
        out_path.write_text(output_svg, encoding="utf-8")
        print(f"Saved SVG: {out_path}")
    else:
        output_png.save(str(out_path), "PNG")
        print(f"Saved PNG: {out_path}  ({output_png.width}×{output_png.height}px)")

    if sidecar:
        sidecar_path = out_path.with_suffix(".json")
        sidecar_path.write_text(_json.dumps(sidecar, indent=2), encoding="utf-8")
        print(f"Saved sidecar: {sidecar_path}")

    return 0


if __name__ == "__main__":
    sys.exit(main())

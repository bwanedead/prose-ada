class TimelineRenderer:
    """
    Renders a ProsAda canvas layout to a PIL Image.

    Parameters
    ----------
    canvas      : Output of CanvasLayoutComposer.compose()
    scale       : Pixels per spine logical unit (default 80)
    img_width   : Total image width in pixels (auto-sized if None)
    title       : Optional title string overlay
    """

    def __init__(
        self,
        canvas: Dict[str, Any],
        scale: int = 80,
        img_width: Optional[int] = None,
        title: Optional[str] = None,
    ):
        self.canvas = canvas
        self.scale = scale
        self.title = title

        # Compute canvas span from all positioned objects
        max_end = self._compute_max_end()
        content_width = max(int(max_end * scale) + 1, 10)

        self.img_width = img_width if img_width else content_width + MARGIN_LEFT + MARGIN_RIGHT
        self.content_width = self.img_width - MARGIN_LEFT - MARGIN_RIGHT

        # Lane assignment: lane 0 = spine, lanes 1..N = streams
        timelines = canvas.get("timelines", {})
        threads = canvas.get("threads", {})
        self.lane_objects: Dict[int, dict] = {}  # laneId → {id, name, color, position, duration}

        for tl in timelines.values():
            lid = tl.get("laneId", 0)
            self.lane_objects[lid] = {"color": "#4a4a6a", **tl}

        for th in threads.values():
            lid = th.get("laneId", 1)
            self.lane_objects[lid] = th

        max_lane = max(self.lane_objects.keys(), default=0)
        self.num_lanes = max_lane + 1

        # Total image height
        lane_area = self.num_lanes * (LANE_HEIGHT + LANE_GAP)
        self.img_height = MARGIN_TOP + lane_area + MARGIN_BOTTOM

    def _compute_max_end(self) -> float:
        ends: List[float] = [1.0]
        for obj in self.canvas.get("timelines", {}).values():
            ends.append(obj.get("position", 0) + obj.get("duration", 1))
        for obj in self.canvas.get("threads", {}).values():
            ends.append(obj.get("position", 0) + obj.get("duration", 1))
        for obj in self.canvas.get("events", {}).values():
            ends.append(obj.get("position", 0) + obj.get("duration", 1))
        return max(ends)

    def _lane_y(self, lane_id: int) -> int:
        """Top-left y coordinate for the given lane row."""
        return MARGIN_TOP + lane_id * (LANE_HEIGHT + LANE_GAP)

    def _spine_x(self, position: float) -> int:
        """Convert spine logical unit to pixel x (including left margin)."""
        return MARGIN_LEFT + int(position * self.scale)

    def _spine_w(self, duration: float) -> int:
        return max(2, int(duration * self.scale))

    def render(self, overlay_data: Optional[Dict[str, Dict]] = None) -> Image.Image:
        """Produce and return the rendered PIL Image.

        Parameters
        ----------
        overlay_data : Optional per-event overlay props (from compute_overlay).
                       Keys are canvas event ids; values are dicts with optional
                       fill_rgb, opacity, outline_rgb, outline_width.
        """
        img = Image.new("RGB", (self.img_width, self.img_height), BG_COLOR)
        draw = ImageDraw.Draw(img, "RGBA")

        self._draw_grid(draw)
        self._draw_title(draw)
        self._draw_lanes(draw)
        self._draw_events(draw, overlay_data=overlay_data)
        self._draw_checkpoints(draw)
        self._draw_lane_labels(draw)

        return img

    def _draw_grid(self, draw: ImageDraw.ImageDraw) -> None:
        """Draw vertical grid lines at each spine integer unit."""
        max_end = self._compute_max_end()
        for i in range(0, int(max_end) + 2):
            x = self._spine_x(i)
            if x > self.img_width - MARGIN_RIGHT:
                break
            draw.line([(x, MARGIN_TOP), (x, self.img_height - MARGIN_BOTTOM)], fill=GRID_COLOR, width=1)
            # Tick label
            draw.text((x + 2, MARGIN_TOP - 14), str(i), fill=TICK_COLOR)

    def _draw_title(self, draw: ImageDraw.ImageDraw) -> None:
        if not self.title:
            return
        draw.text((MARGIN_LEFT, 8), self.title, fill=LABEL_FG)

    def _draw_lanes(self, draw: ImageDraw.ImageDraw) -> None:
        """Draw timeline/thread track bars (colored background strips)."""
        for lane_id, obj in self.lane_objects.items():
            y = self._lane_y(lane_id)
            pos = obj.get("position", 0)
            dur = obj.get("duration", 1)
            raw_color = obj.get("color", "#888888")

            try:
                rgb = _hex_to_rgb(raw_color)
            except Exception:
                rgb = (128, 128, 128)

            x = self._spine_x(pos)
            w = self._spine_w(dur)

            # Track background
            draw.rectangle(
                [(x, y), (x + w, y + LANE_HEIGHT)],
                fill=rgb + (THREAD_TRACK_ALPHA,),
            )
            # Track border (1px top edge)
            draw.line([(x, y), (x + w, y)], fill=rgb + (180,), width=1)

    def _draw_events(
        self,
        draw: ImageDraw.ImageDraw,
        overlay_data: Optional[Dict[str, Dict]] = None,
    ) -> None:
        """Draw event rectangles on their lanes, applying overlay props per event."""
        od_map = overlay_data or {}

        # Build per-lane color from lane_objects
        lane_colors: Dict[int, Tuple[int, int, int]] = {}
        for lane_id, obj in self.lane_objects.items():
            try:
                lane_colors[lane_id] = _hex_to_rgb(obj.get("color", "#888888"))
            except Exception:
                lane_colors[lane_id] = (128, 128, 128)

        # Sort events so spine (lane 0) renders on top of stream copies
        events_sorted = sorted(
            self.canvas.get("events", {}).values(),
            key=lambda e: (e.get("laneId", 0) != 0),  # lane 0 last (painted on top)
        )

        for event in events_sorted:
            ev_id = event.get("id", "")
            od = od_map.get(ev_id, {})

            lane_id = event.get("laneId", 0)
            y_lane = self._lane_y(lane_id)
            pos = event.get("position", 0)
            dur = event.get("duration", 1)
            is_locked = event.get("isResolved", False)

            # Base color
            base_rgb = lane_colors.get(lane_id, (100, 120, 180))
            light_rgb = _lighten(base_rgb, 1.8)

            # Overlay overrides
            fill_rgb: Tuple[int, int, int] = od.get("fill_rgb", light_rgb)
            opacity: float = od.get("opacity", 1.0)
            outline_rgb: Tuple[int, int, int] = od.get(
                "outline_rgb",
                LOCKED_BORDER if is_locked else light_rgb,
            )
            outline_width: int = od.get("outline_width", 2 if is_locked else 1)

            x = self._spine_x(pos)
            w = self._spine_w(dur)
            y_ev = y_lane + EVENT_Y_OFFSET
            h_ev = EVENT_HEIGHT

            ev_alpha = int(EVENT_ALPHA * opacity)

            # Event fill
            draw.rectangle(
                [(x, y_ev), (x + w, y_ev + h_ev)],
                fill=fill_rgb + (ev_alpha,),
                outline=outline_rgb + (255,),
                width=outline_width,
            )

            # Title label (truncated)
            title = event.get("title", "")
            if title and w > 12:
                max_chars = max(1, (w - 4) // 6)
                label = title[:max_chars] + ("…" if len(title) > max_chars else "")
                draw.text((x + 3, y_ev + 2), label, fill=LABEL_FG)

            # Badge text (e.g. shot type abbreviation from overlay)
            badge = od.get("badge_text")
            if badge and w > 8:
                bw = len(badge) * 6 + 4
                bh = 9
                bx = x + w - bw - 2
                by = y_ev + h_ev - bh - 1
                if bx >= x:
                    draw.rectangle(
                        [(bx, by), (bx + bw, by + bh)],
                        fill=(0, 0, 0, 200),
                    )
                    draw.text((bx + 2, by), badge, fill=(255, 255, 200))

    def _draw_checkpoints(self, draw: ImageDraw.ImageDraw) -> None:
        """Draw milestone diamonds (locked events)."""
        for cp in self.canvas.get("checkpoints", {}).values():
            pos = cp.get("position", 0)
            x = self._spine_x(pos)
            y = self._lane_y(0) + LANE_HEIGHT // 2
            s = CHECKPOINT_SIZE
            # Diamond shape
            draw.polygon(
                [(x, y - s), (x + s, y), (x, y + s), (x - s, y)],
                fill=CHECKPOINT_COLOR,
                outline=(255, 255, 255),
            )

    def _draw_lane_labels(self, draw: ImageDraw.ImageDraw) -> None:
        """Draw lane name labels in the left margin."""
        for lane_id, obj in sorted(self.lane_objects.items()):
            y = self._lane_y(lane_id) + LANE_HEIGHT // 2 - 5
            name = obj.get("name", obj.get("id", f"Lane {lane_id}"))
            # Truncate to fit in margin
            max_chars = (MARGIN_LEFT - 8) // 6
            label = name[:max_chars] if len(name) > max_chars else name
            try:
                rgb = _hex_to_rgb(obj.get("color", "#888888"))
            except Exception:
                rgb = (160, 160, 180)
            draw.text((4, y), label, fill=rgb)



def build_render_sidecar(
    project_path: Path,
    canvas: Dict[str, Any],
    renderer_width: int,
    renderer_height: int,
    num_lanes: int,
    max_end: float,
    *,
    scope: Optional[str] = None,
    streams: Optional[List[str]] = None,
    status: Optional[str] = None,
    scale: int = 80,
    format: str = "png",
    overlay_mode: Optional[str] = None,
    overlay_matched_count: Optional[int] = None,
) -> dict:
    """
    Build the render metadata sidecar dict.

    This is written as a JSON file alongside the output image when --sidecar is used.
    """
    from datetime import datetime, timezone

    sidecar: dict = {
        "prosadaPath": str(project_path),
        "renderedAt": datetime.now(timezone.utc).isoformat().replace("+00:00", "Z"),
        "rendererVersion": RENDERER_VERSION,
        "filters": {
            "scope": scope,
            "streams": streams,
            "status": status,
        },
        "renderSettings": {
            "scale": scale,
            "format": format,
        },
        "bounds": {
            "maxEnd": max_end,
            "imgWidth": renderer_width,
            "imgHeight": renderer_height,
            "numLanes": num_lanes,
        },
        "counts": {
            "timelines": len(canvas.get("timelines", {})),
            "threads": len(canvas.get("threads", {})),
            "events": len(canvas.get("events", {})),
            "checkpoints": len(canvas.get("checkpoints", {})),
        },
    }
    if overlay_mode is not None:
        sidecar["overlay"] = {
            "mode": overlay_mode,
            "matchedCount": overlay_matched_count,
        }
    return sidecar



def render_project_with_sidecar(
    project_path: Path,
    *,
    format: str = "png",
    scope: Optional[str] = None,
    streams: Optional[List[str]] = None,
    status: Optional[str] = None,
    scale: int = 80,
    img_width: Optional[int] = None,
    title: Optional[str] = None,
) -> Tuple[Any, dict]:
    """
    Full render pipeline returning (output, sidecar_dict).

    output is PIL.Image for format="png", str for format="svg".
    sidecar_dict is always a JSON-serialisable dict.
    """
    units, manifest, registries_raw, canvas = _load_and_compose(project_path)
    canvas = _apply_canvas_filters(canvas, units, scope, streams, status)

    if format == "svg":
        r = SVGTimelineRenderer(canvas, scale=scale, img_width=img_width, title=title)
        output = r.render_svg()
        w, h, nl = r.img_width, r.img_height, r.num_lanes
        max_end = r._compute_max_end()
    else:
        r = TimelineRenderer(canvas, scale=scale, img_width=img_width, title=title)
        output = r.render()
        w, h, nl = r.img_width, r.img_height, r.num_lanes
        max_end = r._compute_max_end()

    sidecar = build_render_sidecar(
        project_path, canvas, w, h, nl, max_end,
        scope=scope, streams=streams, status=status,
        scale=scale, format=format,
    )
    return output, sidecar



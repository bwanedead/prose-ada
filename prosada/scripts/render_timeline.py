def _overlay_semantic(
    canvas: Dict[str, Any],
    projection: Dict,
    semantic_kind: Optional[str] = None,
    semantic_entity_id: Optional[str] = None,
    **kwargs: Any,
) -> Dict[str, Dict]:
    """
    Semantic entity overlay.

    Matching events (those whose semantic summaries match the kind/entity filter)
    are drawn with a cyan highlight outline at full opacity.
    Non-matching events are dimmed (0.3 opacity).

    With no filter (semantic_kind=None, semantic_entity_id=None):
      events with any semantic refs are highlighted; events with none are dimmed.
    """
    events_payload = {ev["eventId"]: ev for ev in projection.get("events", [])}
    matching_ids: Set[str] = set()

    for ev_id, ev in events_payload.items():
        sem = ev.get("semantic", {})
        if semantic_kind and semantic_entity_id:
            match = (
                sem.get("byKind", {}).get(semantic_kind, 0) > 0
                and semantic_entity_id in sem.get("entityIds", [])
            )
        elif semantic_kind:
            match = sem.get("byKind", {}).get(semantic_kind, 0) > 0
        elif semantic_entity_id:
            match = semantic_entity_id in sem.get("entityIds", [])
        else:
            match = sem.get("refCount", 0) > 0

        if match:
            matching_ids.add(ev_id)

    overlay: Dict[str, Dict] = {}
    has_filter = bool(semantic_kind or semantic_entity_id)
    any_matches = bool(matching_ids)

    for ev_id in canvas.get("events", {}):
        if ev_id in matching_ids:
            overlay[ev_id] = {
                "outline_rgb":   _SEMANTIC_HIGHLIGHT,
                "outline_width": 3,
                "opacity":       1.0,
            }
        elif any_matches or has_filter:
            overlay[ev_id] = {"opacity": _SEMANTIC_DIM_OPACITY}

    return overlay


def _overlay_status(
    canvas: Dict[str, Any],
    projection: Dict,
    **kwargs: Any,
) -> Dict[str, Dict]:
    """
    Execution/status overlay.

    Tints each event bar by its narrative.status:
      draft  → blue-indigo
      review → amber
      locked → green
    """
    ev_status = {
        ev["eventId"]: ev.get("status", "draft")
        for ev in projection.get("events", [])
    }
    overlay: Dict[str, Dict] = {}
    for ev_id in canvas.get("events", {}):
        status = ev_status.get(ev_id, "draft")
        rgb = _STATUS_TINTS.get(status, _STATUS_TINTS["draft"])
        overlay[ev_id] = {"fill_rgb": rgb}
    return overlay


def _overlay_mood(
    canvas: Dict[str, Any],
    projection: Dict,
    **kwargs: Any,
) -> Dict[str, Dict]:
    """
    Presentation mood overlay.

    Tints each event bar by its presentation.moodTags[0] value.
    Events with no presentation data or no moodTags are dimmed.
    Graceful no-op when no units have presentation metadata.
    """
    events_payload = {ev["eventId"]: ev for ev in projection.get("events", [])}
    any_mood = any(
        bool((ev.get("presentation") or {}).get("moodTags"))
        for ev in events_payload.values()
    )

    overlay: Dict[str, Dict] = {}
    for ev_id in canvas.get("events", {}):
        ev = events_payload.get(ev_id, {})
        pres = ev.get("presentation") or {}
        mood_tags = pres.get("moodTags") or []
        first_mood = mood_tags[0].lower() if mood_tags else None

        if first_mood:
            rgb = _MOOD_TINTS.get(first_mood, _MOOD_DEFAULT)
            overlay[ev_id] = {"fill_rgb": rgb, "opacity": 1.0}
        elif any_mood:
            # Dim events that have no mood data when at least some do
            overlay[ev_id] = {"opacity": _MOOD_NO_DATA_OPACITY}

    return overlay


def _overlay_shot(
    canvas: Dict[str, Any],
    projection: Dict,
    **kwargs: Any,
) -> Dict[str, Dict]:
    """
    Presentation shot-type overlay.

    Tints each event bar by its presentation.shotType value and adds a
    short badge text (e.g. 'EST', 'CU', 'POV') rendered inside the bar.
    Events with no shotType are dimmed.
    Graceful no-op when no units have presentation metadata.
    """
    events_payload = {ev["eventId"]: ev for ev in projection.get("events", [])}
    any_shot = any(
        bool((ev.get("presentation") or {}).get("shotType"))
        for ev in events_payload.values()
    )

    overlay: Dict[str, Dict] = {}
    for ev_id in canvas.get("events", {}):
        ev = events_payload.get(ev_id, {})
        pres = ev.get("presentation") or {}
        shot_type = (pres.get("shotType") or "").lower().strip()

        if shot_type:
            rgb = _SHOT_TINTS.get(shot_type, _SHOT_DEFAULT)
            badge = _SHOT_BADGES.get(shot_type, shot_type[:3].upper())
            overlay[ev_id] = {
                "fill_rgb":   rgb,
                "opacity":    1.0,
                "badge_text": badge,
            }
        elif any_shot:
            overlay[ev_id] = {"opacity": _SHOT_NO_DATA_OPACITY}

    return overlay


# Overlay registry — maps overlay name → function
_OVERLAY_REGISTRY: Dict[str, Any] = {
    "semantic": _overlay_semantic,
    "status":   _overlay_status,
    "mood":     _overlay_mood,
    "shot":     _overlay_shot,
}


def compute_overlay(
    overlay_name: str,
    canvas: Dict[str, Any],
    projection: Dict,
    **kwargs: Any,
) -> Dict[str, Dict]:
    """
    Compute overlay data for a canvas + projection pair.

    Parameters
    ----------
    overlay_name : "semantic" | "status" (or any registered name)
    canvas       : composed canvas dict (from CanvasLayoutComposer)
    projection   : projection payload dict (from build_projection)
    **kwargs     : forwarded to the overlay function
                   semantic overlay: semantic_kind=, semantic_entity_id=

    Returns
    -------
    Dict[event_id, overlay_props]
      overlay_props keys (all optional):
        fill_rgb      : (r,g,b) override for event fill
        opacity       : float 0.0..1.0 multiplier for EVENT_ALPHA
        outline_rgb   : (r,g,b) override for event outline
        outline_width : int override for outline stroke width

    Returns empty dict for unknown overlay names (safe no-op).
    """
    fn = _OVERLAY_REGISTRY.get(overlay_name)
    if fn is None:
        return {}
    try:
        return fn(canvas, projection, **kwargs)
    except Exception:
        return {}



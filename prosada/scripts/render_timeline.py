def _load_project(project_path: Path) -> Tuple[Dict, dict, Dict]:
    """
    Load units, manifest, and registries from a prosada project directory.

    Returns (units, manifest, registries_raw)
    Raises FileNotFoundError if the path does not contain manifest.json.
    """
    # Import here to keep top-level imports clean for CLI usage
    import sys
    sys.path.insert(0, str(Path(__file__).parent))
    from persistence.unit_repository import UnitRepository
    from domain.v2_registry import RegistryType

    repo = UnitRepository(project_path)
    manifest = repo.load_manifest()
    units = repo.load_all_units()

    # Build raw registry dicts for color/type lookups
    registries_raw: Dict[str, Any] = {}
    for registry_type in RegistryType:
        rf = repo.load_registry(registry_type)
        if rf is not None:
            registries_raw[registry_type.value] = rf.model_dump(by_alias=True)

    return units, manifest, registries_raw


def _load_and_compose(
    project_path: Path,
) -> Tuple[Dict, Dict, Dict, Dict]:
    """
    Load and compose a project. Returns (units, manifest, registries_raw, canvas).
    Raises FileNotFoundError / ValueError as needed.
    """
    manifest_file = project_path / "manifest.json"
    if not manifest_file.exists():
        raise FileNotFoundError(f"No manifest.json in {project_path}")

    units, manifest, registries_raw = _load_project(project_path)
    if not units:
        raise ValueError(f"No units found in {project_path / 'units'}")

    from services.canvas_layout_composer import CanvasLayoutComposer
    canvas = CanvasLayoutComposer.compose(units, manifest, registries_raw)
    return units, manifest, registries_raw, canvas


